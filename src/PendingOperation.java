
public class PendingOperation {
	
	public PendingOperation(NodeDetail node, String fileName) {
		this.node = node;
		this.fileName = fileName;
	}
	
	private NodeDetail node;
	private String fileName;
	
	public NodeDetail getNode() {
		return node;
	}
	public void setNode(NodeDetail node) {
		this.node = node;
	}
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	@Override
	public String toString() {
		return "PendingOperation [node=" + node + ", fileName=" + fileName + "]";
	}
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((fileName == null) ? 0 : fileName.hashCode());
		result = prime * result + ((node == null) ? 0 : node.hashCode());
		return result;
	}
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		PendingOperation other = (PendingOperation) obj;
		if (fileName == null) {
			if (other.fileName != null)
				return false;
		} else if (!fileName.equals(other.fileName))
			return false;
		if (node == null) {
			if (other.node != null)
				return false;
		} else if (!node.equals(other.node))
			return false;
		return true;
	}
	
}
